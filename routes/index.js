var express = require('express');
var router = express.Router();

router.get('/', function(req, res){
  res.render('index', {
    title: 'Home'
  });
});

router.get('/about', function(req, res){
  res.render('about', {
    title: 'About'
  });
});

router.get('/launch', function(req, res){
  res.render('launch', {
    title: 'Launch'
  });
});

module.exports = router;